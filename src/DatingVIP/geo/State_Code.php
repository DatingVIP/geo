/**
 * Get regions/provinces/states etc for given country code
 *
 * @param string $country_code
 * @access public
 * @return array
 */
    public function getCountry($country_code)
    {
        return $this->isValidCountry ($country_code) ? self::$states[$country_code] : [];
    }

/**
 * Return region name for given country and region code
 *
 * @param string $country_code
 * @param string $state_code
 * @access public
 * @return string
 */
    public function getName($country_code, $state_code)
    {
        return $this->isValid ($country_code, $state_code) ? self::$states[$country_code][$state_code] : '';
    }

/**
 * Return state code for given country and state name
 *
 * @param string $country_code
 * @param string $state_name
 * @access public
 * @return string
 */
    public function get($country_code, $state_name)
    {
        if (!$this->isValidCountry ($country_code))            { return ''; }

        return array_search ($state_name, self::$states[$country_code]) ?: '';
    }

/**
 * Check if we have valid state code for given country code
 *
 * @param string $country_code
 * @param string $state_code
 * @access public
 * @return boolean
 */
    public function isValid($country_code, $state_code)
    {
        return $this->isValidCountry ($country_code) && is_scalar ($state_code) && array_key_exists ((string) $state_code, self::$states[$country_code]);
    }

/**
 * Check if we have valid country code
 *
 * @param string $country_code
 * @access protected
 * @return bool
 */
    protected function isValidCountry($country_code)
    {
        return !empty ($country_code) && is_scalar ($country_code) && array_key_exists ((string) $country_code, self::$states);
    }

} // end of class
